import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";

const root = path.join(path.dirname(fileURLToPath(import.meta.url)), "..");
const urlFile = path.join(root, "strapi.url");
const outFile = path.join(root, "src/lib/strapi-public-url.ts");

const fromEnv = process.env.NEXT_PUBLIC_STRAPI_URL?.trim().replace(/\/$/, "");
const fromFile = fs.existsSync(urlFile)
  ? fs.readFileSync(urlFile, "utf8").trim().replace(/\/$/, "")
  : "";
const url = fromEnv || fromFile || "http://localhost:1337";

fs.writeFileSync(
  outFile,
  `/** Auto-generated by scripts/sync-strapi-url.mjs — edit strapi.url or set NEXT_PUBLIC_STRAPI_URL */\nexport const STRAPI_PUBLIC_URL = ${JSON.stringify(url)};\n`
);

console.log(`Strapi public URL: ${url}`);
